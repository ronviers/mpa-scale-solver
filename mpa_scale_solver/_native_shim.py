def forward_sweep_invert_posterior_wrapped(
    target_substrate, field, tau_obs, *,
    canonical_grid=None,
    noise_variance: float = 1.0,
    k_frust: bool = False,
    score_fn=None,
    top_k: int = 5,
):
    if score_fn is not None:
        from .operations import forward_sweep_invert_posterior_wrapped as _py
        return _py(
            target_substrate, field, tau_obs,
            canonical_grid=canonical_grid,
            noise_variance=noise_variance, k_frust=k_frust,
            score_fn=score_fn, top_k=top_k,
        )
    d = _nat.forward_sweep_invert_posterior_wrapped(
        _to_dict(target_substrate),
        _to_dict(field),
        float(tau_obs),
        _to_dict(canonical_grid) if canonical_grid is not None else None,
        float(noise_variance),
        bool(k_frust),
        int(top_k),
    )
    return _wrap_output(_posterior(d["value"]), d)


def flow(canonical_initial, nu, field):
    # Lookup-table / learned shapes are not supported by the native
    # `flow`; fall back to Python which has consistent error semantics.
    if not isinstance(field, TangentFlowField):
        from .flow import flow as _py
        return _py(canonical_initial, nu, field)
    d = _nat.flow(_to_dict(canonical_initial), float(nu), _to_dict(field))
    return _canonical_state(d)
