# ---------------------------------------------------------------------------
# Input coercion — Python dataclasses → dicts for the native binding.
# ---------------------------------------------------------------------------

def _to_dict(value: Any) -> Any:
    """Recursive `dataclasses.asdict` that passes through dicts / lists /
    scalars unchanged. The native binding accepts JSON-shaped dicts;
    Python consumers usually pass dataclasses.

    `InverseLookupSidecar` is special-cased: its lookup dicts use
    `tuple[float, float, float]` keys which JSON / serde can't carry
    natively. `encode_sidecar_to_json` produces the
    `docs/SIDECAR_FORMAT.md` wire form (`':'`-joined `f64::to_bits`
    decimal-string keys), which the Rust `serde_json::from_str::<
    InverseLookupSidecar>` deserializer (session-7 followup) consumes
    bit-identically."""
    if isinstance(value, InverseLookupSidecar):
        return encode_sidecar_to_json(value)
    if dataclasses.is_dataclass(value) and not isinstance(value, type):
        return dataclasses.asdict(value)
    if isinstance(value, (list, tuple)):
        return [_to_dict(v) for v in value]
    if isinstance(value, dict):
        return {k: _to_dict(v) for k, v in value.items()}
    if hasattr(value, "tolist"):  # numpy array
        return value.tolist()
    return value


# ---------------------------------------------------------------------------
# Output reconstruction — native dicts → typed Python dataclasses.
# ---------------------------------------------------------------------------

def _canonical_state(d: dict) -> CanonicalState:
    return CanonicalState(
        chit=d["chit"],
        gamma_AB=d["gamma_AB"],
        k_frust=d.get("k_frust", False),
    )


def _substrate_state(d: dict) -> SubstrateState:
    return SubstrateState(
        tau_obs=d["tau_obs"],
        label=d.get("label"),
        axes=d.get("axes") or {},
        observables=d.get("observables") or {},
    )


def _regime_reading(d: dict) -> RegimeReading:
    # RegimeLabel is `typing.Literal[...]`, not an Enum — the
    # consumer-facing value is the bare string ("deep_c", "c_near_s",
    # ...). Pass through verbatim.
    return RegimeReading(
        regime=d["regime"],
        k_frust=d.get("k_frust", False),
    )


def _validation_report(d: dict) -> ValidationReport:
    return ValidationReport(
        asymptotic_closure_compliant=d["asymptotic_closure_compliant"],
        k_frust_invariant=d["k_frust_invariant"],
        round_trip_residual=d.get("round_trip_residual"),
        notes=tuple(d.get("notes") or ()),
    )


def _provenance(d: dict) -> Provenance:
    return Provenance(
        solver_version=d["solver_version"],
        operation=d["operation"],
        timestamp_ns=d["timestamp_ns"],
        dispatch_path=DispatchPath(d["dispatch_path"]),
        table_version=d.get("table_version"),
        notes=tuple(d.get("notes") or ()),
    )


def _posterior(d: dict) -> Posterior:
    cov = d["covariance"]
    cov_tuple = (
        (float(cov[0][0]), float(cov[0][1])),
        (float(cov[1][0]), float(cov[1][1])),
    )
    return Posterior(
        mean=_canonical_state(d["mean"]),
        covariance=cov_tuple,
        noise_variance=d.get("noise_variance", 1.0),
        log_evidence=d.get("log_evidence"),
        modes=tuple(_canonical_state(m) for m in (d.get("modes") or ())),
        notes=tuple(d.get("notes") or ()),
    )


# Map intent_id → the `preserved_invariant` string Python's
# operations.py emits in each sacrifice dict. Rust's SacrificeRecord
# carries the string only via the `preserved_invariant()` method
# (session-6 asymmetric-parity decision), so the shim re-injects it on
# the Python side to match the consumer-facing dict shape.
_PRESERVED_INVARIANT = {
    "I1": "regime ∧ sign(gamma_AB) ∧ k_frust",
    "I2": "exact_drive_parameters",
    "I3": "capacity_class ∧ k_frust",
    "I4": "sign(gamma_AB)",
    "I5": "regime_label",
}


def _sacrifice(d: dict) -> dict:
    """Rust SacrificeRecord → Python sac dict. Re-injects the
    `preserved_invariant` string (per-intent) so consumers that read
    `sac['preserved_invariant']` keep working."""
    out = dict(d)
    intent = out.get("intent")
    if intent in _PRESERVED_INVARIANT:
        # Match Python's key-order convention: intent first, then
        # preserved_invariant, then invariant_preserved, then deltas.
        rebuilt = {
            "intent": intent,
            "preserved_invariant": _PRESERVED_INVARIANT[intent],
            "invariant_preserved": out["invariant_preserved"],
            "delta_chit": out["delta_chit"],
            "delta_gamma_AB": out["delta_gamma_AB"],
        }
        # Append intent-specific diagnostic fields in their natural order
        # (drop the keys already placed above).
        placed = set(rebuilt)
        for k, v in out.items():
            if k not in placed:
                rebuilt[k] = v
        return rebuilt
    return out


def _wrap_output(value: Any, d: dict) -> OperationOutput:
    return OperationOutput(
        value=value,
        validation=_validation_report(d["validation"]),
        provenance=_provenance(d["provenance"]),
    )


