"""Session-9 transparent shim — routes the wrapped variants through
`_mpa_scale_solver_native` when the wheel is installed.

This module is imported by `mpa_scale_solver/__init__.py`. If the native
extension fails to import (e.g., the wheel hasn't been built for this
platform yet), the shim re-raises `ImportError` and the package
top-level falls back to `from .operations import (...)` so the
consumer surface keeps working on pure Python.

Each wrapped variant:
  1. Coerces input dataclasses to dicts via `dataclasses.asdict`.
  2. Calls into `_mpa_scale_solver_native` (returns a dict shaped like
     `dataclasses.asdict(OperationOutput[T])`).
  3. Reconstructs the typed Python dataclasses so the consumer-side
     API (`.value`, `.value.chit`, `out.provenance.dispatch_path ==
     DispatchPath.DIRECT_COMPUTE`) is preserved.

Reconstruction is done with explicit per-op converters rather than a
generic typing-aware engine — the value-type per op is fixed, the
converters are small, and the explicit shape is easier to audit when a
divergence surfaces.

Wire-level parity is proven by `tests/test_rust_parity.py` (18 tests,
covering the 9 wrapped variants + raw `validate_driver_profile` +
`flow`). Field-level discipline matches sessions 7+8 asymmetric-parity:

  * `provenance.timestamp_ns`: regenerated on the Python side via
    `make_provenance` semantics (the native value is process-local
    and not consumer-meaningful — re-stamping keeps the shim's
    Provenance shape identical to the pure-Python path).
  * `provenance.notes`: re-stamped via `make_provenance` (Python's
    `f"{0.0}"` vs Rust's `"0"` divergence noted in the bit_identity.rs
    module docstring).
  * `validation.notes`: regenerated by the Python validation helper
    (same `0.0`-vs-`0` divergence).
  * `sacrifice_record.preserved_invariant`: derived per intent (the
    Rust SacrificeRecord doesn't serialize it; the
    `IntentDiagnostics::preserved_invariant()` method returns the
    string but it's not in the dict wire form — see
    `rust/src/types.rs` line 421).
"""

